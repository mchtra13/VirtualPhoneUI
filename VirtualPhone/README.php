# 📱 VirtualPhone - PocketMine-MP Plugin

**VirtualPhone** is a unique plugin for Minecraft Bedrock servers using PocketMine-MP (API 5.x). It gives players a **virtual smartphone** packed with fun and interactive features — all accessible directly in-game!

---

## 🎮 Main Features

| Feature             | Description                                                                 |
|---------------------|-----------------------------------------------------------------------------|
| 📱 Phone Item        | Special item in the last inventory slot — cannot be dropped or lost        |
| 📨 DM                | Send private messages to other players                                     |
| 📬 Inbox             | View incoming messages from other players                                  |
| 💸 Money Transfer    | Instantly send money to another player                                     |
| 🎰 Slot Machine      | Realistic 3-reel slot machine with money rewards only                      |
| 📊 Profile Viewer    | Show your name, online status, balance, and rank                          |
| 🧩 Mini Game         | Simple "Guess the Number" game with money reward                           |
| ⏰ Alarm / Reminder  | Set a personal timer that reminds you with a custom message                |

---

## 🧩 Plugin Highlights

- Fully interactive UI using FormAPI
- Phone item is protected from being moved, dropped, or lost on death
- Slot machine mimics real-life mechanics with spinning reels
- Lightweight and easy to extend with new features

---

## 📥 Installation

1. Download this plugin and place it in your server's `/plugins` folder.
2. Make sure your server is running **PocketMine-MP API 5.x**.
3. Install the required dependencies:
   - [FormAPI](https://github.com/jojoe77777/FormAPI)
   - [BedrockEconomy](https://github.com/cooldogedev/BedrockEconomy)

---

## 🕹️ How to Use

- Players receive a special item called **Virtual Phone** in the last hotbar slot.
- When they interact with it, a **phone-style menu UI** will open.
- Players can choose from:
  - Sending private messages
  - Reading inbox
  - Transferring money
  - Playing slot machine
  - Viewing their profile
  - Setting alarm/reminder

